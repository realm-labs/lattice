use std::collections::{BTreeMap, BTreeSet};

use crate::{CodegenError, ProtoRouteKeyOption, RouteKeyType, RpcMethodSpec};

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct GeneratedRpcBindings {
    pub rust: String,
}

pub fn generate_rpc_bindings(
    methods: &[RpcMethodSpec],
) -> Result<GeneratedRpcBindings, CodegenError> {
    validate_methods(methods)?;

    let mut rust = String::new();
    rust.push_str("// @generated by lattice-codegen\n");
    rust.push_str("use lattice_actor::{Actor, ActorHandle, Handler};\n");
    rust.push_str("use lattice_core::{ActorKind, RouteKey, actor_kind};\n");
    rust.push_str(
        "use lattice_gateway::{GatewayError, GatewayRouteTable, ProstClientMessageBinding};\n",
    );
    rust.push_str(
        "use lattice_rpc::{ActorRpcAdapter, Rpc, RpcRequest, RoutedRequest, ShardedRpcCore, TypedRpcClient};\n\n",
    );

    for method in methods {
        push_routed_request(&mut rust, method);
        push_rpc_request(&mut rust, method);
        push_handler_assertion(&mut rust, method);
    }
    for service_methods in group_by_service(methods).values() {
        push_typed_client(&mut rust, service_methods);
        push_server_adapter(&mut rust, service_methods);
    }
    for method in methods {
        push_gateway_binding(&mut rust, method);
    }
    push_gateway_route_table(&mut rust, methods);

    Ok(GeneratedRpcBindings { rust })
}

fn validate_methods(methods: &[RpcMethodSpec]) -> Result<(), CodegenError> {
    let mut msg_ids = BTreeSet::new();
    for method in methods {
        if method.service_kind.trim().is_empty() {
            return Err(CodegenError::MissingField("service_kind"));
        }
        if method.service_name.trim().is_empty() {
            return Err(CodegenError::MissingField("service_name"));
        }
        if method.method_name.trim().is_empty() {
            return Err(CodegenError::MissingField("method_name"));
        }
        if method.request_type.trim().is_empty() {
            return Err(CodegenError::MissingField("request_type"));
        }
        if method.reply_type.trim().is_empty() {
            return Err(CodegenError::MissingField("reply_type"));
        }
        if method.route_key.actor_kind.trim().is_empty() {
            return Err(CodegenError::MissingField("route_key.actor_kind"));
        }
        if method.route_key.key_field.trim().is_empty() {
            return Err(CodegenError::MissingField("route_key.key_field"));
        }
        if let Some(msg_id) = method.gateway_msg_id
            && !msg_ids.insert(msg_id)
        {
            return Err(CodegenError::DuplicateGatewayMessageId { msg_id });
        }
    }
    Ok(())
}

fn push_routed_request(rust: &mut String, method: &RpcMethodSpec) {
    rust.push_str(&format!(
        "impl RoutedRequest for {request} {{\n",
        request = method.request_type
    ));
    rust.push_str("    fn actor_kind(&self) -> ActorKind {\n");
    rust.push_str(&format!(
        "        actor_kind!(\"{}\")\n",
        method.route_key.actor_kind
    ));
    rust.push_str("    }\n\n");
    rust.push_str("    fn route_key(&self) -> RouteKey {\n");
    rust.push_str(&format!("        {}\n", route_key_expr(&method.route_key)));
    rust.push_str("    }\n");
    rust.push_str("}\n\n");
}

fn push_rpc_request(rust: &mut String, method: &RpcMethodSpec) {
    rust.push_str(&format!(
        "impl RpcRequest for {request} {{\n",
        request = method.request_type
    ));
    rust.push_str(&format!("    type Reply = {};\n", method.reply_type));
    rust.push_str(&format!(
        "    const METHOD: &'static str = \"{}\";\n",
        method.method_path()
    ));
    rust.push_str("}\n\n");
}

fn push_handler_assertion(rust: &mut String, method: &RpcMethodSpec) {
    rust.push_str(&format!(
        "pub fn assert_{snake}_handler<A>()\n",
        snake = lower_camel_to_snake(&method.method_name)
    ));
    rust.push_str("where\n");
    rust.push_str("    A: Actor + Handler<Rpc<");
    rust.push_str(&method.request_type);
    rust.push_str(">>,\n");
    rust.push_str("{\n");
    rust.push_str("}\n\n");
}

fn push_typed_client(rust: &mut String, methods: &[&RpcMethodSpec]) {
    let service_name = &methods[0].service_name;
    let client_name = format!("{service_name}Client");
    rust.push_str(&format!(
        "pub struct {client}<C> {{\n    inner: TypedRpcClient<C>,\n}}\n\n",
        client = client_name
    ));
    rust.push_str(&format!("impl<C> {client}<C>\n", client = client_name));
    rust.push_str("where\n    C: ShardedRpcCore,\n{\n");
    rust.push_str("    pub fn new(core: C) -> Self {\n        Self { inner: TypedRpcClient::new(core) }\n    }\n\n");
    for method in methods {
        rust.push_str(&format!(
            "    pub async fn {method}(&self, req: {request}) -> Result<{reply}, lattice_rpc::RpcError> {{\n",
            method = lower_camel_to_snake(&method.method_name),
            request = method.request_type,
            reply = method.reply_type
        ));
        rust.push_str("        self.inner.call(req).await\n");
        rust.push_str("    }\n");
    }
    rust.push_str("}\n\n");
}

fn push_server_adapter(rust: &mut String, methods: &[&RpcMethodSpec]) {
    let service = methods[0];
    let adapter_name = format!("{}ActorService", service.service_name);
    let trait_path = tonic_service_trait_path(service);
    rust.push_str("#[derive(Clone)]\n");
    rust.push_str(&format!(
        "pub struct {adapter}<A: Actor> {{\n    inner: ActorRpcAdapter<A>,\n}}\n\n",
        adapter = adapter_name
    ));
    rust.push_str(&format!(
        "impl<A: Actor> {adapter}<A> {{\n",
        adapter = adapter_name
    ));
    rust.push_str("    pub fn new(handle: ActorHandle<A>) -> Self {\n");
    rust.push_str("        Self { inner: ActorRpcAdapter::new(handle) }\n");
    rust.push_str("    }\n");
    rust.push_str("}\n\n");
    rust.push_str("#[tonic::async_trait]\n");
    rust.push_str(&format!(
        "impl<A> {trait_path} for {adapter}<A>\n",
        adapter = adapter_name
    ));
    rust.push_str("where\n    A: Actor + Sync");
    for method in methods {
        rust.push_str(" + Handler<Rpc<");
        rust.push_str(&method.request_type);
        rust.push_str(">>");
    }
    rust.push_str(",\n{\n");
    for method in methods {
        rust.push_str(&format!(
            "    async fn {method}(&self, request: tonic::Request<{request}>) -> Result<tonic::Response<{reply}>, tonic::Status> {{\n",
            method = lower_camel_to_snake(&method.method_name),
            request = method.request_type,
            reply = method.reply_type
        ));
        rust.push_str("        self.inner.unary(request).await\n");
        rust.push_str("    }\n");
    }
    rust.push_str("}\n\n");
}

fn push_gateway_binding(rust: &mut String, method: &RpcMethodSpec) {
    let binding_name = format!(
        "{}{}GatewayBinding",
        method.service_name, method.method_name
    );
    rust.push_str(&format!("pub struct {};\n\n", binding_name));
    rust.push_str(&format!("impl {} {{\n", binding_name));
    if let Some(msg_id) = method.gateway_msg_id {
        rust.push_str(&format!(
            "    pub const DEFAULT_MSG_ID: u32 = {};\n\n",
            msg_id
        ));
        rust.push_str("    pub fn default_binding() -> ProstClientMessageBinding<");
        rust.push_str(&method.request_type);
        rust.push_str("> {\n");
        rust.push_str("        Self::binding(Self::DEFAULT_MSG_ID)\n");
        rust.push_str("    }\n\n");
    }
    rust.push_str("    pub fn binding(msg_id: u32) -> ProstClientMessageBinding<");
    rust.push_str(&method.request_type);
    rust.push_str("> {\n");
    rust.push_str("        ProstClientMessageBinding::new(msg_id)\n");
    rust.push_str("    }\n");
    rust.push_str("}\n\n");
}

fn push_gateway_route_table(rust: &mut String, methods: &[RpcMethodSpec]) {
    let gateway_methods: Vec<&RpcMethodSpec> = methods
        .iter()
        .filter(|method| method.gateway_msg_id.is_some())
        .collect();
    if gateway_methods.is_empty() {
        return;
    }

    rust.push_str(
        "pub fn register_gateway_routes(table: &mut GatewayRouteTable) -> Result<(), GatewayError> {\n",
    );
    for method in gateway_methods {
        rust.push_str("    table.register(");
        rust.push_str(&format!(
            "{}{}GatewayBinding::default_binding().route_spec()",
            method.service_name, method.method_name
        ));
        rust.push_str(")?;\n");
    }
    rust.push_str("    Ok(())\n");
    rust.push_str("}\n\n");
}

fn group_by_service(methods: &[RpcMethodSpec]) -> BTreeMap<(&str, &str), Vec<&RpcMethodSpec>> {
    let mut grouped = BTreeMap::new();
    for method in methods {
        grouped
            .entry((method.package.as_str(), method.service_name.as_str()))
            .or_insert_with(Vec::new)
            .push(method);
    }
    grouped
}

fn route_key_expr(option: &ProtoRouteKeyOption) -> String {
    match option.key_type {
        RouteKeyType::U64 => format!("RouteKey::U64(self.{})", option.key_field),
        RouteKeyType::I64 => format!("RouteKey::I64(self.{})", option.key_field),
        RouteKeyType::String => format!("RouteKey::Str(self.{}.clone())", option.key_field),
        RouteKeyType::Bytes => format!("RouteKey::Bytes(self.{}.clone())", option.key_field),
    }
}

fn tonic_service_trait_path(method: &RpcMethodSpec) -> String {
    let module = package_module(&method.package);
    let server_module = format!("{}_server", lower_camel_to_snake(&method.service_name));
    if module.is_empty() {
        format!("crate::{server_module}::{}", method.service_name)
    } else {
        format!("crate::{module}::{server_module}::{}", method.service_name)
    }
}

fn package_module(package: &str) -> String {
    package
        .split('.')
        .filter(|part| !part.is_empty())
        .collect::<Vec<_>>()
        .join("::")
}

fn lower_camel_to_snake(value: &str) -> String {
    let mut output = String::new();
    for (index, ch) in value.chars().enumerate() {
        if ch.is_ascii_uppercase() {
            if index > 0 {
                output.push('_');
            }
            output.push(ch.to_ascii_lowercase());
        } else {
            output.push(ch);
        }
    }
    output
}
