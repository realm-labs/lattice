#[derive(Debug, Clone, PartialEq, Eq)]
pub struct GeneratedRpcBindings {
    pub rust: String,
}

#[derive(Debug, Clone, Copy, Default, PartialEq, Eq)]
pub struct RenderOptions {
    pub actor_ref_proto: bool,
}

pub fn generate_rpc_bindings(
    methods: &[RpcMethodSpec],
) -> Result<GeneratedRpcBindings, CodegenError> {
    generate_rpc_bindings_with_options(methods, RenderOptions::default())
}

pub fn generate_rpc_bindings_with_options(
    methods: &[RpcMethodSpec],
    options: RenderOptions,
) -> Result<GeneratedRpcBindings, CodegenError> {
    validate_methods(methods)?;

    let mut rust = String::new();
    rust.push_str("// @generated by lattice-codegen\n");
    rust.push_str("#[allow(unused_imports)]\nuse std::sync::Arc;\n");
    rust.push_str("#[allow(unused_imports)]\nuse lattice_actor::{Actor, ActorHandle, ActorLoader, ActorRegistry, Handler};\n");
    rust.push_str(
        "#[allow(unused_imports)]\nuse lattice_core::{ActorId, ActorKind, RouteKey, actor_kind};\n",
    );
    rust.push_str(
        "#[allow(unused_imports)]\nuse lattice_gateway::{ClientFrame, GatewayError, GatewayRouteTable};\n",
    );
    rust.push_str(
        "#[allow(unused_imports)]\nuse lattice_placement::{EndpointLease, EndpointRpcTransport};\n",
    );
    rust.push_str(
        "#[allow(unused_imports)]\nuse lattice_rpc::{ActorRpcAdapter, Rpc, RpcError, RpcRequest, RoutedRequest, ShardedRpcCore, TonicEndpointChannelPool, TypedRpcClient};\n",
    );
    rust.push_str("#[allow(unused_imports)]\nuse prost::Message as ProstMessage;\n\n");

    if options.actor_ref_proto {
        push_actor_ref_conversions(&mut rust);
    }
    for method in methods {
        push_routed_request(&mut rust, method);
        push_rpc_request(&mut rust, method);
    }
    for service_methods in group_by_service(methods).values() {
        push_service_module(&mut rust, service_methods);
    }
    push_tonic_endpoint_transport(&mut rust, methods);
    push_gateway_route_table(&mut rust, methods);
    push_gateway_dispatcher(&mut rust, methods);

    Ok(GeneratedRpcBindings { rust })
}

